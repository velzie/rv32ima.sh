#include "config_ast.h"  // IWYU pragma: keep

#include <stdlib.h>
#include <string.h>

//
// Return a copy of s of n chars using malloc.
//
void *memdup(const void *s, size_t n) {
    void *t = malloc(n);
    return memcpy(t, s, n);
}
